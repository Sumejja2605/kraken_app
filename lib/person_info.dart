class PersonInfo {
  final String name;
  final String title;
  final int phoneNumber;
  final String eMail;
  final String? gitHubUrl;
  final String twUrl;
  final String liUrl;

  PersonInfo({
    required this.name,
    required this.title,
    required this.phoneNumber,
    required this.eMail,
    required this.gitHubUrl,
    required this.twUrl,
    required this.liUrl,
  });
}
